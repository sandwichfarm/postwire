#!/usr/bin/env node
// scripts/bench-to-readme.mjs
//
// Runs `pnpm bench:fast` (unless --skip-bench), parses baseline.json, renders
// a compact Markdown table, and rewrites README.md between `<!-- bench:start -->`
// / `<!-- bench:end -->` sentinels. Fail-loud on bench failure or missing/empty
// baseline; idempotent (byte-identical output on re-run with same inputs).
// See .planning/quick/260421-ukg-*/260421-ukg-PLAN.md. Zero deps, Node 22 built-ins.

import { spawnSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";

const START = "<!-- bench:start -->";
const END = "<!-- bench:end -->";
const BASELINE_PATH = "benchmarks/results/baseline.json";
const README_PATH = "README.md";

// --- args --------------------------------------------------------------------
// Accept `--` as a bare POSIX end-of-options marker (pnpm forwards it before
// script args, e.g. `pnpm bench:readme -- --skip-bench`).
const args = process.argv.slice(2);
const skipBench = args.includes("--skip-bench");
const unknown = args.filter((a) => a !== "--skip-bench" && a !== "--");
if (unknown.length > 0) {
  console.error(`bench-to-readme: unknown argument(s): ${unknown.join(" ")}`);
  console.error("usage: node scripts/bench-to-readme.mjs [--skip-bench]");
  process.exit(2);
}

// --- 1. run benchmarks (must precede any README write) -----------------------
if (!skipBench) {
  const r = spawnSync("pnpm", ["bench:fast"], { stdio: "inherit", shell: false });
  if (r.error || r.status !== 0) {
    console.error("bench-to-readme: `pnpm bench:fast` failed — not touching README.");
    process.exit(1);
  }
}

// --- 2. load + validate baseline.json ----------------------------------------
let artifact;
try {
  artifact = JSON.parse(readFileSync(BASELINE_PATH, "utf8"));
} catch (err) {
  console.error(`bench-to-readme: cannot read/parse ${BASELINE_PATH}: ${err.message}`);
  process.exit(1);
}
if (!artifact || !Array.isArray(artifact.scenarios) || artifact.scenarios.length === 0) {
  console.error(`bench-to-readme: ${BASELINE_PATH} has no scenarios — aborting.`);
  process.exit(1);
}

// --- 3. de-dup by name (last-seen wins — duplicates expected per STATE.md) ---
const byName = new Map();
for (const s of artifact.scenarios) byName.set(s.name, s);
const scenarios = Array.from(byName.values());

// --- 4. group by family + sort by payload size -------------------------------
const FAMILIES = [
  { key: "transferable", label: "library (transferable)", match: "library (transferable)" },
  { key: "sab", label: "library (SAB)", match: "library (SAB)" },
  { key: "clone", label: "library (structured-clone)", match: "library (structured-clone)" },
  { key: "naive", label: "naive postMessage", match: "naive postMessage" },
];
const grouped = new Map(FAMILIES.map((f) => [f.key, []]));
const other = [];
for (const s of scenarios) {
  const fam = FAMILIES.find((f) => s.name.startsWith(f.match));
  if (fam) grouped.get(fam.key).push(s);
  else other.push(s);
}
for (const arr of grouped.values()) arr.sort((a, b) => a.payloadBytes - b.payloadBytes);
other.sort((a, b) => a.payloadBytes - b.payloadBytes);

// --- 5. render Markdown table -------------------------------------------------
const formatBytes = (n) => {
  if (n >= 1024 * 1024) return `${n / (1024 * 1024)} MB`;
  if (n >= 1024) return `${n / 1024} KB`;
  return `${n} B`;
};
const fmt = (v) => (typeof v === "number" && Number.isFinite(v) ? v.toFixed(2) : "—");
const fmtInt = (v) =>
  typeof v === "number" && Number.isFinite(v) ? v.toLocaleString("en-US") : "—";

const header = [
  "| Scenario | Payload | Throughput (MB/s) | p50 (ms) | p99 (ms) | Samples |",
  "|---|---|---:|---:|---:|---:|",
];
const rowsOut = [];
let total = 0;
const emit = (arr, label) => {
  for (const s of arr) {
    rowsOut.push(
      `| ${label} | ${formatBytes(s.payloadBytes)} | ${fmt(s.mb_s)} | ${fmt(s.p50_ms)} | ${fmt(s.p99_ms)} | ${fmtInt(s.samples)} |`,
    );
    total++;
  }
};
for (const f of FAMILIES) {
  const arr = grouped.get(f.key);
  if (arr.length > 0 && rowsOut.length > 0) rowsOut.push("| | | | | | |");
  emit(arr, f.label);
}
if (other.length > 0) {
  if (rowsOut.length > 0) rowsOut.push("| | | | | | |");
  emit(other, "other");
}

const envLine = `_Environment: Node ${artifact.node || "?"} · MessageChannel (${artifact.env || "?"}) · commit ${artifact.commit || "?"} · ${artifact.timestamp || "unknown"}_`;
const genLine = "_Generated by `scripts/bench-to-readme.mjs` — do not edit by hand._";
const footnote = [
  "> **On the `naive postMessage` row.** This baseline is a single raw `ArrayBuffer` transfer per message — no framing, no ordering, no backpressure, no multiplexing, no relay. It measures the ceiling of the underlying transport, not a comparable alternative. postwire layers stream semantics on top of that transport (ordered delivery, credit-window backpressure, multiplexed streams, multi-hop relay via structured-clone-only hops) that raw `postMessage` cannot provide. The honest question is _\"does this overhead fit my budget for the features I need?\"_ — not _\"is it faster than the transport it is built on?\"_ It is not, and cannot be.",
].join("\n");
const block = [envLine, "", ...header, ...rowsOut, "", footnote, "", genLine].join("\n");

// --- 6. rewrite README between sentinels (idempotent; fail-loud on malformed)-
const readme = readFileSync(README_PATH, "utf8");
const hasStart = readme.includes(START);
const hasEnd = readme.includes(END);
if (hasStart !== hasEnd) {
  console.error(
    `bench-to-readme: README has exactly one of ${START}/${END} — refusing to guess. Fix manually.`,
  );
  process.exit(1);
}

let next;
if (hasStart && hasEnd) {
  const i = readme.indexOf(START);
  const j = readme.indexOf(END, i + START.length);
  if (j < i) {
    console.error("bench-to-readme: sentinels are out of order in README — refusing to guess.");
    process.exit(1);
  }
  next = `${readme.slice(0, i + START.length)}\n${block}\n${readme.slice(j)}`;
} else {
  const insertion = `## Benchmarks\n\n${START}\n${block}\n${END}\n\n`;
  const licenseIdx = readme.indexOf("\n## License");
  if (licenseIdx !== -1) {
    next = `${readme.slice(0, licenseIdx + 1)}${insertion}${readme.slice(licenseIdx + 1)}`;
  } else {
    const sep = readme.endsWith("\n") ? "\n" : "\n\n";
    next = `${readme}${sep}${insertion}`;
  }
}

const tag = `env=${artifact.env}, node=${artifact.node}, commit=${artifact.commit}`;
if (next === readme) {
  console.log(`README.md unchanged (${total} scenarios, ${tag})`);
  process.exit(0);
}
writeFileSync(README_PATH, next);
console.log(`Updated README.md with ${total} scenarios (${tag})`);
